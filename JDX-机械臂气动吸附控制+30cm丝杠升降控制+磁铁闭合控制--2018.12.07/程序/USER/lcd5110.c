/*--------------------------------------------
LCD_write_byte: 使用SPI接口写数据到LCD
输入参数：dt：写入的数据；
command ：写数据/命令选择；
编写日期：20110804
----------------------------------------------*/
void LCD_write_byte(unsigned char dt, unsigned char command)
{
	unsigned char i; 
	GPIO_WriteBit(GPIOE, GPIO_Pin_3, 0);//片选   sce=0;
	delayms(1);
	
	GPIO_WriteBit(GPIOE, GPIO_Pin_4, command);//1写数据，0写指令     dc=command;
	//delay1(1);
	delayms(1);


	for(i=0;i<8;i++)
	{ 
		if(dt&0x80)
	GPIO_WriteBit(GPIOE, GPIO_Pin_5, 1);//数据		sdin=1;
		else
	GPIO_WriteBit(GPIOE, GPIO_Pin_5, 0);//数据		sdin=0;
		dt=dt<<1;
		delayms(1);


	GPIO_WriteBit(GPIOE, GPIO_Pin_6, 0);//时钟	sclk=0; 
		delayms(1);


	GPIO_WriteBit(GPIOE, GPIO_Pin_6, 1);//时钟	sclk=1; 
	}	
	GPIO_WriteBit(GPIOE, GPIO_Pin_4, 1);//1写数据，0写指令 dc=1;	
	GPIO_WriteBit(GPIOE, GPIO_Pin_3, 1);//片选             sce=1;	
	GPIO_WriteBit(GPIOE, GPIO_Pin_5, 1);//数据             sdin=1;
	
	delayms(1);
	
	 	
}
