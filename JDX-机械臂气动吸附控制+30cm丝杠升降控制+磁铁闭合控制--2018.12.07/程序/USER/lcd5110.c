	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz; 		//设置引脚速率为50MHz */ 
	GPIO_Init(GPIOC, &GPIO_InitStructure);	                //调用库函数，初始化GPIOB

	RCC_APB2PeriphClockCmd( RCC_APB2Periph_GPIOE, ENABLE); 	//开启GPIOB的外设时钟

