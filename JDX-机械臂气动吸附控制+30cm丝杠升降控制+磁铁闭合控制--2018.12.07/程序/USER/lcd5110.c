void LCD_init(void)
{
    //产生一个让LCD复位的低电平脉冲
    NOK_5110_GPIO_Config();
    delayms(200);
   	GPIO_WriteBit(GPIOE, GPIO_Pin_2, 0);//复位,0复位   res=0;  	   
  	delayms(200);
  	GPIO_WriteBit(GPIOE, GPIO_Pin_2, 1);//复位,0复位   res=1;  
  	
	  LCD_write_byte(0x21,0);//初始化Lcd,功能设定使用扩充指令
	  LCD_write_byte(0xd0,0);//设定液晶偏置电压
	  LCD_write_byte(0x20,0);//使用基本指令
	  LCD_write_byte(0x0C,0);//设定显示模式，正常显示
}
